package operators;

public class AssignmentOperator {

        public static void main(String[] args) {
            // Example 1: Assigning value to int variable
            int intValue = 10;
            System.out.println("intValue: " + intValue); // Output: intValue: 10

            // Example 2: Assigning value to double variable
            double doubleValue = 3.14;
            System.out.println("doubleValue: " + doubleValue); // Output: doubleValue: 3.14

            // Example 3: Assigning value to boolean variable
            boolean booleanValue = true;
            System.out.println("booleanValue: " + booleanValue); // Output: booleanValue: true

            // Example 4: Assigning value to char variable
            char charValue = 'A';
            System.out.println("charValue: " + charValue); // Output: charValue: A

            // Example 5: Assigning value to String variable
            String stringValue = "Hello, world!";
            System.out.println("stringValue: " + stringValue); // Output: stringValue: Hello, world!
        }
    }


