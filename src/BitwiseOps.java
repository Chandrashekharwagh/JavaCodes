public class BitwiseOps {

        public static void main(String[] args) {
            // Bitwise AND (&)
            int num1 = 5;
            int num2 = 10;

            // Example 1: Bitwise AND
            int resultAnd1 = num1 & num2;
            System.out.println("Bitwise AND Example 1: " + resultAnd1);

            // Example 2: Bitwise AND
            int num3 = 15;
            int resultAnd2 = num2 & num3;
            System.out.println("Bitwise AND Example 2: " + resultAnd2);

            // Bitwise OR (|)
            // Example 1: Bitwise OR
            int resultOr1 = num1 | num2;
            System.out.println("Bitwise OR Example 1: " + resultOr1);

            // Example 2: Bitwise OR
            int num4 = 20;
            int resultOr2 = num3 | num4;
            System.out.println("Bitwise OR Example 2: " + resultOr2);
        }
    }


